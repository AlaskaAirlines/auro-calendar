import { fixture, html, expect } from '@open-wc/testing';
import '../src/auro-calendar.js';

describe('auro-calendar', () => {
  it('sets the CSS class on auro-calendar > div element', async () => {
    const el = await fixture(html`
      <auro-calendar cssclass="testClass"></auro-calendar>
    `);

    const div = el.shadowRoot.querySelector('div');
    expect(div.className).to.equal('testClass');
  });

  it('auro-calendar is accessible', async () => {
    const el = await fixture(html`
      <auro-calendar cssclass="testClass"></auro-calendar>
    `);

    await expect(el).to.be.accessible();
  });

  it('auro-calendar custom element is defined', async () => {
    const el = await !!customElements.get("auro-calendar");

    await expect(el).to.be.true;
  });
});
